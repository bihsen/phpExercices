<?php
$bool1 = true;
$bool2 = false;

// You can use && / AND
if ($bool1 && $bool2)
    echo "this is true";


// You can use || / OR
if ($bool1 || $bool2)
    echo "this is true";


// You can use XOR
if ($bool1 xor $bool2)
    echo "this is true";
    
    // You can use ! / NOT
    if (!$bool1)
        echo "this is true";
?>